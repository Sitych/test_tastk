#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	size_t	i;

	i = 0;
	if (!s)
		return ;
	while (s[i] != '\0')
	{
		if (write(fd, &s[i], 1) == -1)
			return ;
		i++;
	}
}
